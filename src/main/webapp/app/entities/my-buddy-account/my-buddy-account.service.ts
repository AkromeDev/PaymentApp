import axios from 'axios';

import { IMyBuddyAccount } from '@/shared/model/my-buddy-account.model';

const baseApiUrl = 'api/my-buddy-accounts';

export default class MyBuddyAccountService {
  public find(id: number): Promise<IMyBuddyAccount> {
    return new Promise<IMyBuddyAccount>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IMyBuddyAccount): Promise<IMyBuddyAccount> {
    return new Promise<IMyBuddyAccount>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IMyBuddyAccount): Promise<IMyBuddyAccount> {
    return new Promise<IMyBuddyAccount>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
